// Parallax effect for planets
function initParallax() {
    window.addEventListener('scroll', function() {
        const scrolled = window.pageYOffset;
        const planets = document.querySelectorAll('.planet');
        
        planets.forEach((planet, index) => {
            const speed = 0.5 + (index * 0.1);
            const yPos = -(scrolled * speed);
            planet.style.transform = `translateY(${yPos}px)`;
        });
    });
}

// Typing effect for hero title
function initTypingEffect() {
    const heroTitle = document.querySelector('.hero-title');
    if (heroTitle) {
        const text = heroTitle.textContent;
        heroTitle.textContent = '';
        
        let i = 0;
        const typeWriter = () => {
            if (i < text.length) {
                heroTitle.textContent += text.charAt(i);
                i++;
                setTimeout(typeWriter, 100);
            }
        };
        
        // Start typing after a delay
        setTimeout(typeWriter, 1000);
    }
}

// Particle effect for stars
function initParticleEffect() {
    const starsContainer = document.querySelector('.stars');
    if (starsContainer) {
        // Create additional stars dynamically
        for (let i = 0; i < 50; i++) {
            const star = document.createElement('div');
            star.style.cssText = `
                position: absolute;
                width: 2px;
                height: 2px;
                background: white;
                border-radius: 50%;
                left: ${Math.random() * 100}%;
                top: ${Math.random() * 100}%;
                animation: twinkle ${2 + Math.random() * 3}s ease-in-out infinite;
                animation-delay: ${Math.random() * 2}s;
            `;
            starsContainer.appendChild(star);
        }
    }
}

// Language switcher functionality
function initLanguageSwitcher() {
    const langButtons = document.querySelectorAll('.lang-btn');
    
    langButtons.forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            changeLanguage(lang);
            
            // Update active button
            langButtons.forEach(b => b.classList.remove('active'));
            this.classList.add('active');
        });
    });
}

// Change language function
function changeLanguage(lang) {
    currentLang = lang;
    
    // Update HTML lang and dir attributes
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
    
    // Update all translatable elements
    const translatableElements = document.querySelectorAll('[data-translate]');
    translatableElements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update placeholders
    const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    // Save language preference
    localStorage.setItem('preferred-language', lang);
    
    // Update page title and meta description
    updatePageMeta(lang);
}

// Update page meta information
function updatePageMeta(lang) {
    const titles = {
        tr: "Profesyonel Portföy - Web Geliştirici",
        en: "Professional Portfolio - Web Developer",
        fa: "پورتفولیو حرفه‌ای - توسعه‌دهنده وب"
    };
    
    const descriptions = {
        tr: "Django, Python, JavaScript, CSS, HTML ve Bootstrap konularında uzman profesyonel web geliştirici",
        en: "Professional web developer expert in Django, Python, JavaScript, CSS, HTML and Bootstrap",
        fa: "توسعه‌دهنده حرفه‌ای وب با تخصص در Django، Python، JavaScript، CSS، HTML و Bootstrap"
    };
    
    document.title = titles[lang] || titles.tr;
    document.querySelector('meta[name="description"]').setAttribute('content', descriptions[lang] || descriptions.tr);
}

// Load saved language preference
function loadSavedLanguage() {
    const savedLang = localStorage.getItem('preferred-language');
    if (savedLang && translations[savedLang]) {
        changeLanguage(savedLang);
        
        // Update active button
        const langButtons = document.querySelectorAll('.lang-btn');
        langButtons.forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === savedLang) {
                btn.classList.add('active');
            }
        });
    }
}

